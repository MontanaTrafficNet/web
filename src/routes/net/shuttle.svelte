  <p>
    All members of the seven-person crew of space<br />
    shuttle Columbia are feared dead after the spacecraft<br />
    broke up high over Texas.. Columbia was on<br />
    its way to what had been considered an uneventful<br />
    landing at Kennedy Space Center in Florida. Three<br />
    of the crew members on the scientific mission were<br />
    Amateur Radio operators. NASA says communication<br />
    was lost around 8 AM Central Time 02/01/03, and confirmed <br />
    reports indicated that debris has been spotted on the<br />
    ground in Texas. Aboard was the first Israeli to fly in space.
  </p>

